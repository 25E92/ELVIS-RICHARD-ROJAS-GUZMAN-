def suma(x,y):
    return x+y
def resta(x,y):
    return x-y
def multiplicacion(x,y):
    return x*y
def division(x,y):
    return x/y
x = 10
y = 5
print('%d + %d = %d' % (x, y, suma(x, y)))
print('%d - %d = %d' % (x, y, resta(x, y)))
print('%d * %d = %d' % (x, y, multiplicacion(x, y)))
print('%d / %d = %d' % (x, y, division(x, y)))

